    private void btCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCalcularActionPerformed
        // TODO add your handling code here:

        String carrera = (String) cbCarrera.getSelectedItem();
        String ciclo = (String) cbCiclo.getSelectedItem();
        String nombre = tfNombre.getText();

        if (isInputValid(carrera, ciclo, nombre)) {
            float[] notas = {
                Float.parseFloat(tfNota1.getText()),
                Float.parseFloat(tfNota2.getText()),
                Float.parseFloat(tfNota3.getText()),
                Float.parseFloat(tfNota4.getText()),
                Float.parseFloat(tfNota5.getText())
            };

            int[] uvs = {
                Integer.parseInt((String) cbUv1.getSelectedItem()),
                Integer.parseInt((String) cbUv2.getSelectedItem()),
                Integer.parseInt((String) cbUv3.getSelectedItem()),
                Integer.parseInt((String) cbUv4.getSelectedItem()),
                Integer.parseInt((String) cbUv5.getSelectedItem())
            };

            int reprobado = 0;
            int aprobado = 0;
            float cum = calculateCUM(notas, uvs);

            for (int i = 0; i < notas.length; i++) {
                JLabel lbEstado = getLabelByIndex(i + 1);

                if (notas[i] < 6) {
                    setLabelStatus(lbEstado, "Reprobado", Color.red);
                    reprobado++;
                } else {
                    setLabelStatus(lbEstado, "Aprobado", Color.green);
                    aprobado++;
                }
            }

            String TotalCum = Float.toString(cum);
            String MateriasAprobadas = Integer.toString(aprobado);
            String MateriasReprobadas = Integer.toString(reprobado);

            tfCum.setText(TotalCum);

            lbMensaje.setText(
                nombre + " su estado actual es de " + MateriasAprobadas + " materias aprobadas y " + MateriasReprobadas +
                " reprobadas y su CUM actual es de " + tfCum.getText()
            );
        }
    }//GEN-LAST:event_btCalcularActionPerformed

