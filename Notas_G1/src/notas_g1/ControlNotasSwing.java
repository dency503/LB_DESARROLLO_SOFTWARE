  private boolean isInputValid(String carrera, String ciclo, String nombre) {
    boolean isValid = true;

    if ("Elegir una opción".equals(carrera) || "Elegir una opción".equals(ciclo) || nombre.isEmpty()) {
        isValid = false;
        JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    return isValid;
}

    private float calculateCUM(float[] notas, int[] uvs) {
    float totalNotas = 0;
    int totalUVs = 0;

    for (int i = 0; i < notas.length; i++) {
        totalNotas += notas[i] * uvs[i];
        totalUVs += uvs[i];
    }

    return totalNotas / totalUVs;
}

private JLabel getLabelByIndex(int index) {
        return switch (index) {
            case 1 -> lbEstado1;
            case 2 -> lbEstado2;
            case 3 -> lbEstado3;
            case 4 -> lbEstado4;
            case 5 -> lbEstado5;
            default -> null;
        };
}

private void setLabelStatus(JLabel label, String text, Color color) {
    label.setOpaque(true);
    label.setText(text);
    label.setBackground(color);
}

